#include "../include/libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*nxt;

	nxt = lst;
	while (nxt)
	{
		if (nxt->next)
			nxt = nxt->next;
		else
			return (nxt);
	}
	return (NULL);
}
